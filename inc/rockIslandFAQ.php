    <span id="tab19" tabindex="0" class="accordion__title" aria-controls="panel6" role="tab" aria-selected="false" aria-expanded="false" data-binding="expand-accordion-trigger">
      <h5>Can I take a canoe, kayak or boat to Rock Island?  </h5>
    </span>

    <div id="panel19" class="accordion__content" role="tabpanel" aria-hidden="true" aria-labelledby="tab6" data-binding="expand-accordion-container">
      <div class="accordion__content-inner">
        <p>Yes, you may beach paddleboats near your campsite. Larger craft may moor near the boathouse,
          depending upon available dock space. Jackson Harbor has a free boat launch ramp at the Rock
          Island State Park dock on Washington Island. </p>
        <p><strong>Safety Warning!</strong> For your safety, please be aware of impending weather,
          wind and wave conditions. Currents, and even submerged rocks, may also present a hazard.
          Always wear your personal flotation device when boating and let others know where you’re
          headed and what your objectives are. </p>
        <p><a href="#" class="faqLink">Download Kayak Guide</a></p>
