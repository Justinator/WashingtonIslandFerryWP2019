      <h5>What about medical emergencies? </h5>
    </span>

    <div id="panel18" class="accordion__content" role="tabpanel" aria-hidden="true" aria-labelledby="tab6" data-binding="expand-accordion-container">
      <div class="accordion__content-inner">
        <p>Call 911! First aid can be administered with help of Rock Island State Park staff, or at the
          Washington Island Clinic. Serious medical emergencies will require the aid of the Washington
          Island Rescue Squad, Clinic Staff (and sometimes the U.S. Coast Guard). Patients may be
          transported to the mainland from Detroit Harbor by special ferry to Northport. Rock Island’s
          State Park ranger, when notified, will help emergency services by alerting the necessary
          emergency care providers. </p>
      </div>
    </div>
